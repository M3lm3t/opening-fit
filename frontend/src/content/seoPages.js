export const guideSeoPagesBySlug = Object.fromEntries(
  guideSeoPages.map((page) => [page.slug, page])
);

export function getGuideSeoPage(slug) {
  return guideSeoPagesBySlug[String(slug || "").toLowerCase()] || null;
}

export function getGuideSeoPageFromPath(path) {
  const match = String(path || "").match(/^\/guides\/([^/]+)$/);
  return match ? getGuideSeoPage(match[1]) : null;
}
